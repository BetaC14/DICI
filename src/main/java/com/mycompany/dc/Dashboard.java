public void actualizarEstadoMenu(javax.swing.JButton botonActivo, String nombreSeccion) {
    // 1. Cambiar el texto del título principal dinámicamente
    jLabelTitulo.setText("-" + nombreSeccion); 
    // Si tienes otra etiqueta más arriba para el estado secundario, la cambias aquí también.

    // 2. Definir los colores (Tu azul original y el color de "Seleccionado")
    java.awt.Color azulOriginal = new java.awt.Color(21, 101, 192);
    java.awt.Color azulSeleccionado = new java.awt.Color(13, 71, 161); // Un azul más oscuro elegante para resaltar

    // 3. Recorrer TODOS los componentes del panel izquierdo para limpiarlos
    for (java.awt.Component comp : jPanel2.getComponents()) {
        if (comp instanceof javax.swing.JButton) {
            javax.swing.JButton btn = (javax.swing.JButton) comp;
            
            // Excluimos los botones de acción como "Editar", "Eliminar" o "Crear Nuevo"
            String texto = btn.getText();
            if (!texto.equals("Editar Botón") && !texto.equals("Eliminar Botón") && !texto.equals("Crear Botón Nuevo")) {
                
                // Si es el botón que el usuario acaba de tocar, lo pintamos diferente
                if (btn == botonActivo) {
                    btn.setBackground(azulSeleccionado);
                } else {
                    // Si no, le devolvemos su color base
                    btn.setBackground(azulOriginal);
                }
            }
        }
    }
    
    // 4. Refrescar el panel para aplicar los cambios de color de inmediato
    jPanel2.revalidate();
    jPanel2.repaint();
}
