    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    java.util.Properties props = new java.util.Properties();
    java.io.File archivo = new java.io.File("botones_personalizados.properties");

    if (!archivo.exists()) {
        JOptionPane.showMessageDialog(this, "No hay botones personalizados para eliminar.", "Información", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // 1. Cargar los botones actuales desde el archivo
    try (java.io.FileInputStream fis = new java.io.FileInputStream(archivo)) {
        props.load(fis);
    } catch (java.io.IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al leer el archivo de configuración.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 🔥 CALCULAR CUÁNTOS BOTONES REALES HAY (sin contar las claves .id)
    int cantidadBotones = 0;
    while (props.containsKey("boton_" + cantidadBotones)) {
        cantidadBotones++;
    }

    if (cantidadBotones == 0) {
        JOptionPane.showMessageDialog(this, "No hay botones personalizados para eliminar.", "Información", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // 2. Pasar los nombres de los botones a un arreglo
    String[] nombresBotones = new String[cantidadBotones];
    for (int i = 0; i < cantidadBotones; i++) {
        nombresBotones[i] = props.getProperty("boton_" + i);
    }

    // 3. Preguntar al usuario cuál desea borrar
    String botonSeleccionado = (String) JOptionPane.showInputDialog(
            this,
            "Seleccione el botón que desea eliminar:",
            "Eliminar Botón",
            JOptionPane.QUESTION_MESSAGE,
            null,
            nombresBotones,
            nombresBotones[0]
    );

    if (botonSeleccionado == null) return;

    int indiceAEliminar = -1;
    for (int i = 0; i < cantidadBotones; i++) {
        if (nombresBotones[i].equals(botonSeleccionado)) {
            indiceAEliminar = i;
            break;
        }
    }

    if (indiceAEliminar == -1) return;

    // 🔥 OBTENER EL ID REAL DE LA PLANTILLA (desde el .properties si existe, o calcular)
    int idPlantillaABorrar = 5 + indiceAEliminar; // valor por defecto
    String idProp = props.getProperty("boton_" + indiceAEliminar + ".id");
    if (idProp != null) {
        try {
            idPlantillaABorrar = Integer.parseInt(idProp);
        } catch (NumberFormatException e) {
            // fallback
        }
    }

    Connection con = ConexionSQL.getConexion();

    if (con == null) {
        JOptionPane.showMessageDialog(this, "Error de conexión a la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 4. Eliminar de la base de datos
    try {
        con.setAutoCommit(false);

        // Desactivar restricciones temporalmente
        try (java.sql.Statement stmt = con.createStatement()) {
            stmt.execute("PRAGMA foreign_keys = OFF;");
        }

        // Eliminar valores_celda de documentos de esta plantilla
        String sqlDeleteValores = "DELETE FROM valores_celda WHERE id_documento IN (SELECT id_documento FROM documentos_planilla WHERE id_plantilla = ?)";
        try (java.sql.PreparedStatement psDeleteV = con.prepareStatement(sqlDeleteValores)) {
            psDeleteV.setInt(1, idPlantillaABorrar);
            psDeleteV.executeUpdate();
            System.out.println("✅ Valores_celda eliminados para plantilla " + idPlantillaABorrar);
        }

        // Eliminar documentos_planilla de esta plantilla
        String sqlDeleteFormatos = "DELETE FROM documentos_planilla WHERE id_plantilla = ?";
        try (java.sql.PreparedStatement psDeleteF = con.prepareStatement(sqlDeleteFormatos)) {
            psDeleteF.setInt(1, idPlantillaABorrar);
            psDeleteF.executeUpdate();
            System.out.println("✅ Documentos eliminados para plantilla " + idPlantillaABorrar);
        }

        // Eliminar campos_plantilla de esta plantilla
        String sqlDeleteCampos = "DELETE FROM campos_plantilla WHERE id_plantilla = ?";
        try (java.sql.PreparedStatement psDeleteC = con.prepareStatement(sqlDeleteCampos)) {
            psDeleteC.setInt(1, idPlantillaABorrar);
            psDeleteC.executeUpdate();
            System.out.println("✅ Campos eliminados para plantilla " + idPlantillaABorrar);
        }

        // Eliminar la plantilla
        String sqlDeletePlantilla = "DELETE FROM plantillas WHERE id_plantilla = ?";
        try (java.sql.PreparedStatement psDeleteP = con.prepareStatement(sqlDeletePlantilla)) {
            psDeleteP.setInt(1, idPlantillaABorrar);
            psDeleteP.executeUpdate();
            System.out.println("✅ Plantilla " + idPlantillaABorrar + " eliminada");
        }

        // Reactivar restricciones
        try (java.sql.Statement stmt = con.createStatement()) {
            stmt.execute("PRAGMA foreign_keys = ON;");
        }

        con.commit();
        System.out.println("✅ BD sincronizada: Plantilla eliminada correctamente.");

    } catch (java.sql.SQLException e) {
        try {
            try (java.sql.Statement stmt = con.createStatement()) {
                stmt.execute("PRAGMA foreign_keys = ON;");
            }
            con.rollback();
        } catch (java.sql.SQLException ex) {
            ex.printStackTrace();
        }

        System.err.println("❌ Error en la transacción: " + e.getMessage());
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Error al eliminar: " + e.getMessage(),
                "Error BD", JOptionPane.ERROR_MESSAGE);
        return;
    } finally {
        try {
            con.setAutoCommit(true);
        } catch (java.sql.SQLException ex) {
            ex.printStackTrace();
        }
    }

    // 5. Reestructurar el archivo .properties (USANDO EL CONTADOR REAL)
    java.util.Properties nuevasProps = new java.util.Properties();
    int nuevoContador = 0;
    for (int i = 0; i < cantidadBotones; i++) {
        if (i != indiceAEliminar) {
            // Copiar el nombre
            nuevasProps.setProperty("boton_" + nuevoContador, nombresBotones[i]);
            // Copiar el ID si existe
            String idOrig = props.getProperty("boton_" + i + ".id");
            if (idOrig != null) {
                nuevasProps.setProperty("boton_" + nuevoContador + ".id", idOrig);
            }
            nuevoContador++;
        }
    }

    try (java.io.FileOutputStream fos = new java.io.FileOutputStream(archivo)) {
        nuevasProps.store(fos, "Botones creados por el usuario");
    } catch (java.io.IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al guardar el archivo de configuración.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    refrescarBarraLateral();
    JOptionPane.showMessageDialog(this, "✅ Botón eliminado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                                         
    java.util.Properties props = new java.util.Properties();
    java.io.File archivo = new java.io.File("botones_personalizados.properties");
    
    if (!archivo.exists()) {
        javax.swing.JOptionPane.showMessageDialog(this, "No hay botones personalizados para editar.", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    
    // 1. Cargar las propiedades actuales
    try (java.io.FileInputStream fis = new java.io.FileInputStream(archivo)) {
        props.load(fis);
    } catch (java.io.IOException e) {
        e.printStackTrace();
    }
    
    if (props.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "No hay botones personalizados para editar.", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        return;
    }
    
    String[] nombresBotones = new String[props.size()];
    for (int i = 0; i < props.size(); i++) {
        nombresBotones[i] = props.getProperty("boton_" + i);
    }
    
    // 2. Preguntar cuál botón quiere renombrar
    String botonSeleccionado = (String) javax.swing.JOptionPane.showInputDialog(
        this,
        "Seleccione el botón que desea editar:",
        "Editar Botón",
        javax.swing.JOptionPane.QUESTION_MESSAGE,
        null,
        nombresBotones,
        nombresBotones[0]
    );
    
    if (botonSeleccionado != null) {
        // 3. Pedir el nuevo nombre de carpeta al usuario
        String nuevoNombre = javax.swing.JOptionPane.showInputDialog(
            this,
            "Ingrese el nuevo nombre para el botón:",
            "Renombrar Botón",
            javax.swing.JOptionPane.QUESTION_MESSAGE
        );
        
        if (nuevoNombre != null && !nuevoNombre.trim().isEmpty()) {
            // Buscar la clave correspondiente y actualizarle el nombre de texto
            for (int i = 0; i < nombresBotones.length; i++) {
                if (nombresBotones[i].equals(botonSeleccionado)) {
                    props.setProperty("boton_" + i, nuevoNombre.trim());
                    break;
                }
            }
            
            // Guardar los cambios con el nuevo nombre de etiqueta en el archivo
            try (java.io.FileOutputStream fos = new java.io.FileOutputStream(archivo)) {
                props.store(fos, "Botones creados por el usuario");
            } catch (java.io.IOException e) {
                e.printStackTrace();
            }
            
            // 🌟 CLAVE: Refresca el panel lateral para cambiar el texto del botón visualmente 
            // manteniendo el mismo ID y la misma acción del controlador
            refrescarBarraLateral();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Botón actualizado correctamente.", "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }

    }//GEN-LAST:event_jButton4ActionPerformed

