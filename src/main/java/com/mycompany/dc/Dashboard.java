private void guardarBotonEnArchivo(String nombre, int idPlantilla) {
    java.util.Properties props = new java.util.Properties();
    java.io.File archivo = new java.io.File("botones_personalizados.properties");
    
    if (archivo.exists()) {
        try (java.io.FileInputStream fis = new java.io.FileInputStream(archivo)) {
            props.load(fis);
        } catch (java.io.IOException e) {
            e.printStackTrace();
        }
    }
    
    // Contar solo las claves "boton_X" (sin .id)
    int cantidadActual = 0;
    while (props.containsKey("boton_" + cantidadActual)) {
        cantidadActual++;
    }
    
    props.setProperty("boton_" + cantidadActual, nombre);
    props.setProperty("boton_" + cantidadActual + ".id", String.valueOf(idPlantilla));
    
    try (java.io.FileOutputStream fos = new java.io.FileOutputStream(archivo)) {
        props.store(fos, "Botones creados por el usuario");
    } catch (java.io.IOException e) {
        e.printStackTrace();
    }
}
